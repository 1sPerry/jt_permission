package jt.entity;

/**
 * - 模块
 *
 * @author wangxiaopan
 * @date 2019/10/23 9:30
 */
public class Module {
    private int id;  //模块ID
    private String moduleName;  //模块名称

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getModuleName() {
        return moduleName;
    }

    public void setModuleName(String moduleName) {
        this.moduleName = moduleName;
    }
}
